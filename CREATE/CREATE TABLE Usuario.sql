Create table Dissaor.Usuario(Id_usuario INT AUTO_INCREMENT NOT NULL,
NombreUser VARCHAR(55) NOT NULL,
ApellidoP VARCHAR(55) NOT NULL,
ApellidoM VARCHAR(55) NOT NULL,
Id_Tusuario INT NOT NULL, 
Telefono VARCHAR(10) NOT NULL,
Email Varchar(255) NOT NULL,
Passwrd VARCHAR(255) NOT NULL,
PRIMARY KEY(Id_usuario)) ENGINE = INNODB;